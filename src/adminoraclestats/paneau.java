    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if (tableTableS.getSelectedRow() < 0) 
        {   
            JOptionPane.showMessageDialog(this, "Sélectionnez un table space SVP", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel) t_objTS.getModel();
            NettoyerTableAndFormulaire(t_objTS);
            t_objTS.setOpaque(true);
            String TS = (String)tableTableS.getValueAt(tableTableS.getSelectedRow(), 1);
            
            String sql ="SELECT segment_name, "
                        + "segment_type, "
                        + "owner , "
                        + "blocks "
                        + "FROM sys.dba_segments  where tablespace_name = '"+TS+"' "
                        + "order by segment_type";
            
            try
            {   
                ResultSet rs = BD.execRequette(sql);
                while(rs.next())
                {
                    String s = rs.getString("segment_name");
                    String t = rs.getString("segment_type");
                    String u = rs.getString("owner");
                    int b = rs.getInt("blocks");
                    
                    model.addRow(new Object[]{s, t, u, b});
                }

            }
            catch (SQLException ex) {
                System.out.println("ERREUR : "+ex.getMessage() );
            }
            catch (NullPointerException ex) {
                System.out.println("NullPointerException ERREUR : "+ex.getMessage() );
            }
            finally
            {
                BD.fermerConnexion();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed
     public void NettoyerTableAndFormulaire(JTable tab) 
    {
        DefaultTableModel model = (DefaultTableModel) tab.getModel();

        while (model.getRowCount() > 0) 
        {
            model.removeRow(0);
        }
    }  
    

