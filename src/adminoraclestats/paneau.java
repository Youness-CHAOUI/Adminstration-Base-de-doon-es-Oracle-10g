    private void comboObjItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboObjItemStateChanged
        
        String[] colHeads = {comboObj.getSelectedItem().toString(), "Date de création"};
        DefaultTableModel tabMode = new DefaultTableModel(null, colHeads);
        tabObjets.setModel(tabMode);
        
        DefaultTableModel model = (DefaultTableModel) tabObjets.getModel();
        NettoyerTableAndFormulaire(tabObjets);
        
        String obj = comboObj.getSelectedItem().toString();
        switch(obj)
        {
            case "Les tables": obj = "TABLE"; break;
            case "Les séquences": obj = "SEQUENCE"; break;
            case "Les vues": obj = "VIEW"; break;
            case "Les synonymes": obj = "SYNONYM"; break;
            case "Les index": obj = "INDEX"; break;
            default : obj =  comboObj.getSelectedItem().toString(); break;
        }
        
        if(userCombo.getSelectedIndex() != -1 )
        try
        {   
            ResultSet rs = BD.execRequette("select OBJECT_NAME, TIMESTAMP from dba_objects where object_name "
                                            + "not like 'BIN$%' and owner like '"+userCombo.getSelectedItem().toString()+"'"
                                            + " AND OBJECT_TYPE like '"+obj+"'");
            while(rs.next())
            {
                model.addRow(new Object[]{rs.getString("OBJECT_NAME"), rs.getString("TIMESTAMP")});
            }

        }
        catch (SQLException ex) {
            System.out.println("ERREUR : "+ex.getMessage() );
        }
        finally
        {
            BD.fermerConnexion();
        }
    }//GEN-LAST:event_comboObjItemStateChanged

