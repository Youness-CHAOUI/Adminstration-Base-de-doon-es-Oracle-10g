    private void tableSpaceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSpaceMouseClicked
        
        if( tableSpace.getTitleAt(tableSpace.getSelectedIndex()) == "Capacité" )
        {
            DefaultTableModel model = (DefaultTableModel) tableTableS.getModel();
            NettoyerTableAndFormulaire(tableTableS);
            
            String sql ="SELECT a.file_id id, a.tablespace_name ts, a.bytes/1024/1024 er, " +
                        "count(*) nbf " +
                        "FROM sys.dba_data_files a, sys.dba_free_space b " +
                        "WHERE a.file_id = b.file_id " +
                        "AND a.tablespace_name = b.tablespace_name " +
                        "GROUP BY a.file_id, a.tablespace_name, a.bytes " +
                        "ORDER BY 1";
            
            ResultSet rs = BD.execRequette(sql);
            try
            {
                while(rs.next())
                {
                    int id = rs.getInt("id");
                    String ts = rs.getString("ts");
                    double er = rs.getDouble("er");
                    int nbf = rs.getInt("nbf");
                    

                    model.addRow(new Object[]{id, ts, er, nbf});
                }

            }
            catch (SQLException ex) {
                System.out.println("ERREUR : "+ex.getMessage() );
            }
            finally
            {
                BD.fermerConnexion();
            }
        }
        
        if( tableSpace.getTitleAt(tableSpace.getSelectedIndex()) == "Utilisateurs" )
        {
            bc_userTS gr = new bc_userTS();
            p_userTS.removeAll();
            p_userTS.add(gr.getPanel());
        }
    }//GEN-LAST:event_tableSpaceMouseClicked

